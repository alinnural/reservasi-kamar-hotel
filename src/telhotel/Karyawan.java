    private void cmdTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdTambahActionPerformed
        // TODO add your handling code here:
        if(cmdTambah.getText().equals("Add")){
            aktif(true);
            tblKar.setEnabled(false);
            clear();
            actButton(false);
            edit=false;
        }
        else if(cmdTambah.getText().equals("Save")){
            String karID=txtKarID.getText();
            String name=txtNamaKar.getText();
            String address=txtAddress.getText();
            String phone=txtPhoneKar.getText();
            String userID=txtUserID.getText();
            String pass=txtPass.getText();
            
        try{
                if (edit==true)
                {
                    stm.executeUpdate("update karyawan set name='"+name+"',address='"+address+"',phonenumber='"+phone+"',jabatan='"+cmbJabatan.getSelectedItem()+"',userid='"+userID+"',passid='"+pass+"' where idkaryawan='"+karID+"'");
                    
                }else
                {
                    stm.executeUpdate("INSERT into karyawan VALUES('"+name+"','"+karID+"','"+address+"','"+phone+"','"+cmbJabatan.getSelectedItem()+"','"+userID+"','"+pass+"')");
                }
                tblKar.setModel(new DefaultTableModel(dataTable,header));
                baca_data();
                actButton(true);
                aktif(false);
                pnlLogin.setVisible(false);
                tblKar.setEnabled(true);
                cmdEdit.setEnabled(false);
                cmdHapus.setEnabled(false);
            }catch(SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_cmdTambahActionPerformed

