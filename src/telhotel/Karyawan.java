    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try{
            String password = new String(txtPassword.getPassword());
            stm = Con.createStatement();
            RsUser=stm.executeQuery("select * from karyawan where userID='"+
                txtUser.getText()+"' and passID= '"+password+"'");
            int baris=0;
            while(RsUser.next()) {
                baris = RsUser.getRow();
                txtPass.setText(RsUser.getString("passID"));
            }
            if(baris==1)//mengecek ada berapa files dengan pass dan user itu, kalo satu benar kalo 0 salah
            {
                JOptionPane.showMessageDialog(null, "Please edit your data!");
                aktif(true);
                tblKar.setEnabled(false);
                pnlLogin.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Invalid, please try again! "+txtUser.getText());
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

