    private void baca_data(){
        try{
                stm = Con.createStatement();
                RsCheckIn = stm.executeQuery("select * from karyawan");
                ResultSetMetaData meta = RsCheckIn.getMetaData();
                int col = meta.getColumnCount();
                int baris = 0;
                while(RsCheckIn.next()) {
                    baris = RsCheckIn.getRow();
                }
                dataTable = new Object[baris][col];
                int x = 0;
                RsCheckIn.beforeFirst();
                while(RsCheckIn.next()) {
                    dataTable[x][0] = RsCheckIn.getString("idkaryawan");
                    dataTable[x][1] = RsCheckIn.getString("name");
                    dataTable[x][2] = RsCheckIn.getString("address");
                    dataTable[x][3] = RsCheckIn.getString("phonenumber");
                    dataTable[x][4] = RsCheckIn.getString("jabatan");
                    dataTable[x][5] = RsCheckIn.getString("userid");
                    x++;
                }
                tblKar.setModel(new DefaultTableModel(dataTable,header));
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
